@hit = 0
@slope_x = 0
@slope_y = 0
@choice = "y"
@multiply = []

def forest_check
  treeline = @forest[@y]
  until @y + 1 == @forest.length
    @x += @slope_x
    if @x > treeline.length - 1
      @x -= treeline.length
    end
    @y += @slope_y
    treeline = @forest[@y]
    if treeline[@x] == "#"
      @hit += 1
    end
  end
  @multiply.push(@hit)
end

def choose_ski
  puts "Ski? (y/n)"
  @choice = gets.chomp
end

def get_slope
  print "x: "
  @slope_x = gets.chomp.to_i
  print "y: "
  @slope_y = gets.chomp.to_i
end

def reset_values
  @hit = 0
  @x = 0
  @y = 0
  @slope_x = 0
  @slope_y = 0
end

def custom_ski
  reset_values
  get_slope
  forest_check
end

def multiply
  puts @multiply.inject(:*)
end

def ski_loop
  loop do
    choose_ski
    if @choice == "y"
      custom_ski
    elsif @choice != "y"
      multiply
      exit
     end
    puts @multiply
  end
end

